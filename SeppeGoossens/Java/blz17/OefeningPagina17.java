import java.lang.*;

public class OefeningPagina17{

	public static void main(String args[])
	{
		System.out.println(~10);
		
		/*
		De 10 wordt door een NOT-poort gestuurd, net zoals de eerste stap bij de omzetting van een getal met two's complement.
		Bij die omzetting verandert je positieve getal naar een negatief getal en wordt de absolute waarde met 1 vermeerderd
		(wat je in stap twee compleseert door er 1 bij op tellen).
		 */
	}

}